# 자료형 : 숫자형
# 변수에 할당되는 값의 형태로 자료형이 결정
# 암묵적으로 자료형이 결정
# 변수 = 숫자리터럴


a = 10
b = -1234
c = 0

print('a=', a)
print('b=', b)
print('c=', c)

# type : 자료형을 알고 싶을 때
# type(변수)
# type(리터럴)
print('a=', type(a))
print('b=', type(b))
print('c=', type(c))
print(type(1234))
print(type('1234'))

