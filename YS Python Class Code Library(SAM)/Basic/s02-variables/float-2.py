# 실수형(float)
# 지수형태

# 10의 3승(10이 3번 곱해진 값) : 1000
# 4.24 * 10의 3승
# 결과 : 4240.0
a = 4.24E3
b = 4.24 * 1000
print('a=', a)
print('b=', b)

#%%
# 10의 10승
x = 10000000000
y = 1.0E10
z = 10 * 10 * 10 * 10 * 10 * 10 * 10 * 10 * 10 * 10
print('x=', x)
print('y=', y, type(y))
print('z=', z, type(z))

#%%

# 1.0 * 10의 -1승
a = 1.0e-1
print('a=', a)

#%%
# 0.123 * 10의 -3승
# 값 : 0.000123
c = 0.123E-3
print('c=', c)

#%%

d = 0.123e3
print('d=',d)