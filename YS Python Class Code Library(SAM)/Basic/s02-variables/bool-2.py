# bool
# True 또는 False 값을 가질 수 있다.

# 같다 : ==
# 같지않다 : !=

# 결과는 bool 자료형
a = (1 == 1)
b = (1 != 1)
c = (a == b)
d = (a != b)
s = ('홍길동' == '홍길동')
x = d != s

print('a:', a, type(a))
print('b:', b, type(b))
print('c:', c, type(c))
print('d:', d, type(d))
print('s:', s, type(s))
print('x:', x, type(x))
