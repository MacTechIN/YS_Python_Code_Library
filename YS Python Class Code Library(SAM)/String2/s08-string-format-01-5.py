# 문자열 포매팅(String Formatting)
# 문자열 포맷 코드
# %s : 문자열(string)
# %c : 문자 1개(character)
# %d : 정수(decimal)
# %f : 실수,부동소수(float)
# %o : 8진수(octal)
# %x : 16진수(hexa-decimal)
#
# 포맷문자열 % 변수
# 포맷문자열 % (변수, ...)



# 부동 소숫점(%f) : float
eco = 3.45
print("올해의 한국 경제 성장율 예상치는 (%d) 입니다." % eco) # 결과: 3
print("올해의 한국 경제 성장율 예상치는 (%f) 입니다." % eco) # 결과: 3.450000


